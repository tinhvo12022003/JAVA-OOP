    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        Customer c = new Customer();
        
        String ma_KH = (String) this.jComboBox4.getSelectedItem();
        
        ArrayList<Customer> lst_customer = new ArrayList<>(c.readFile_Customer());

        for (Customer cus : lst_customer) {
            if (cus.getMa_KH().equals(ma_KH)) {
                this.jTextField12.setText(cus.getHoten());
                this.jTextField3.setText(cus.getAddress().getSoNha() + " " + cus.getAddress().getXa() + " " + cus.getAddress().getHuyen() + " " + cus.getAddress().getTinh());
                this.jTextField8.setText(cus.getDate().getNgay() + "/" + cus.getDate().getThang() + "/" + cus.getDate().getNam());
                this.jTextField9.setText(cus.getPhone());
                this.jTextField10.setText(cus.getMa_KH());
                
                break;
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

