// Hàm để cập nhật số ngày tối đa trong jComboBox3
    public void updateDays() {
        Date d = new Date();
        d.setNam(Integer.parseInt((String) this.jComboBox1.getSelectedItem()));
        d.setThang(Integer.parseInt((String) this.jComboBox2.getSelectedItem()));
        int maxNgay = d.maxNgay(d.getThang(), d.getNam());

        // Xóa tất cả các mục hiện tại trong jComboBox3
        this.jComboBox3.removeAllItems();

        // Thêm các ngày từ 1 đến maxNgay vào jComboBox3
        for (int i = 1; i <= maxNgay; i++) {
            this.jComboBox3.addItem(Integer.toString(i));
        }
    }

    public void getInfo_customer() {
        Customer c = new Customer();

        Date date = new Date();
        date.setNam(Integer.parseInt((String) this.jComboBox1.getSelectedItem()));
        date.setThang(Integer.parseInt((String) this.jComboBox2.getSelectedItem()));
        date.setNgay(Integer.parseInt((String) this.jComboBox3.getSelectedItem()));

        Address addr = new Address();
        addr.setSoNha(new String(this.jTextField2.getText()));
        addr.setXa(new String(this.jTextField5.getText()));
        addr.setHuyen(new String(this.jTextField6.getText()));
        addr.setTinh(new String(this.jTextField7.getText()));

        int max = 9999;
        int min = 1000;
        int range = max - min + 1;
        int rand = (int) (Math.random() * range) + min;

        c.setHoten(this.jTextField1.getText());
        c.setAddress(addr);
        c.setDate(date);
        c.setPhone(new String(this.jTextField4.getText()));
        c.setMa_KH(new String(this.jTextField4.getText()) + rand);

        try {
            //mo file ghi tiep vao cuoi file
            FileWriter writer = new FileWriter("E:\\Buoi5B2111903\\customer.txt", true);
            writer.write(c.getHoten() + "\n" + c.getAddress() + "\n" + c.getDate() + "\n" + c.getPhone() + "\n" + c.getMa_KH() + "\n");
            writer.flush();
            writer.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());

        }
    }

